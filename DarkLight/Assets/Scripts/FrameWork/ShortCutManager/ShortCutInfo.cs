public class ShortCutInfo
{
    private int shortCutID;
    public int ShortCutID
    {
        get
        {
            return shortCutID;
        }

        set
        {
            shortCutID = value;
        }
    }

    private int skillID;
    public int SkillID
    {
        get
        {
            return skillID;
        }

        set
        {
            skillID = value;
        }
    }
}
